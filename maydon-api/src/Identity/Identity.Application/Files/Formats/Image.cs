namespace Identity.Application.Files.Formats;

internal sealed class Image : FileFormatDescriptor
{
	protected override void Initialize()
	{
		TypeName = "IMAGE FILE";
		Extensions.UnionWith([".png", ".jpeg", ".jpg", ".webp"]);
		MagicNumbers.AddRange(new byte[][]
		{
			 [0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A],
			 [0xFF, 0xD8, 0xFF, 0xE0],
			 [0xFF, 0xD8, 0xFF, 0xE1],
			 [0xFF, 0xD8, 0xFF, 0xE2],
			 [0xFF, 0xD8, 0xFF, 0xE3],
			 [0x57, 0x45, 0x42, 0x50],
			 [0x52, 0x49, 0x46, 0x46],
		});
	}
}
